package entity;

public class Gamer {
	private int id_gamer;
	private String name_gamer;
	private String team_name_gamer;
	private String profile_gamer;
	private String settings_gamer;
	private int evaluation_gamer;


	public int getId_gamer() {
		return id_gamer;
	}
	public void setId_gamer(int id_gamer) {
		this.id_gamer = id_gamer;
	}
	public String getName_gamer() {
		return name_gamer;
	}
	public void setName_gamer(String name_gamer) {
		this.name_gamer = name_gamer;
	}
	public String getTeam_name_gamer() {
		return team_name_gamer;
	}
	public void setTeam_name_gamer(String team_name_gamer) {
		this.team_name_gamer = team_name_gamer;
	}
	public String getProfile_gamer() {
		return profile_gamer;
	}
	public void setProfile_gamer(String profile_gamer) {
		this.profile_gamer = profile_gamer;
	}
	public String getSettings_gamer() {
		return settings_gamer;
	}
	public void setSettings_gamer(String settings_gamer) {
		this.settings_gamer = settings_gamer;
	}
	public int getEvaluation_gamer() {
		return evaluation_gamer;
	}
	public void setEvaluation_gamer(int evaluation_gamer) {
		this.evaluation_gamer = evaluation_gamer;
	}

}
